package io.espiral.beans;

public class Mundo {
	String saludoMundo;

	String saludo;
	String nombre;

	public String getSaludoMundo() {
		return saludoMundo;
	}

	public void setSaludoMundo(String saludoMundo) {
		this.saludoMundo = saludoMundo;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getSaludo() {
		return saludo;
	}

	public void setSaludo(String saludo) {
		this.saludo = saludo;
	}
}
